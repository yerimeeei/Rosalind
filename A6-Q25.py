GAP = 5  # linear gap penalty


def linear_space_score(v, w):
    """Return the last row of global alignment scores between v and w"""
    m = len(v)
    prev = [i * -GAP for i in range(m + 1)]
    for j in range(1, len(w) + 1):
        curr = [-GAP * j] + [0] * m
        for i in range(1, m + 1):
            match = prev[i - 1] + BLOSUM62[v[i - 1]][w[j - 1]]
            delete = prev[i] - GAP
            insert = curr[i - 1] - GAP
            curr[i] = max(match, delete, insert)
        prev = curr
    return prev


def hirschberg(v, w):
    """Return optimal alignment of v and w using Hirschberg's algorithm"""
    if len(v) == 0:
        return '-' * len(w), w
    if len(w) == 0:
        return v, '-' * len(v)
    if len(v) == 1 or len(w) == 1:
        # Use simple full DP for small subproblems
        return needleman_wunsch(v, w)

    mid = len(w) // 2
    scoreL = linear_space_score(v, w[:mid])
    scoreR = linear_space_score(v[::-1], w[mid:][::-1])
    max_i = max(range(len(v) + 1),
                key=lambda i: scoreL[i] + scoreR[len(v) - i])

    vL, wL = hirschberg(v[:max_i], w[:mid])
    vR, wR = hirschberg(v[max_i:], w[mid:])
    return vL + vR, wL + wR


def needleman_wunsch(v, w):
    """Simple full DP alignment for small strings"""
    m, n = len(v), len(w)
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    for i in range(1, m + 1):
        dp[i][0] = dp[i - 1][0] - GAP
    for j in range(1, n + 1):
        dp[0][j] = dp[0][j - 1] - GAP

    for i in range(1, m + 1):
        for j in range(1, n + 1):
            dp[i][j] = max(
                dp[i - 1][j - 1] + BLOSUM62[v[i - 1]][w[j - 1]],
                dp[i - 1][j] - GAP,
                dp[i][j - 1] - GAP,
            )

    # Traceback
    i, j = m, n
    aligned_v, aligned_w = "", ""
    while i > 0 or j > 0:
        if i > 0 and j > 0 and dp[i][j] == dp[i - 1][j - 1] + BLOSUM62[v[i - 1]][w[j - 1]]:
            aligned_v = v[i - 1] + aligned_v
            aligned_w = w[j - 1] + aligned_w
            i -= 1
            j -= 1
        elif i > 0 and dp[i][j] == dp[i - 1][j] - GAP:
            aligned_v = v[i - 1] + aligned_v
            aligned_w = '-' + aligned_w
            i -= 1
        else:
            aligned_v = '-' + aligned_v
            aligned_w = w[j - 1] + aligned_w
            j -= 1

    return aligned_v, aligned_w


